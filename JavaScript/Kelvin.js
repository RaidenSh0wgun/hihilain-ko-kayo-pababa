const K = 283
console.log("The temperature is " + K + " kelvin")

const C = K - 273
console.log("The temperature is " + C + " degrees celsius")

const F = C*9/5 + 32
console.log("The temperature is " + F + " degrees fahrenheit")

const N = C*33/100
console.log("The temperature is " + N + " degrees newton")